// 1-bit tileable fill patterns. Every pattern is stored as a 32×32 row-major
// bitmap (1 = on / prints black, 0 = off / transparent). The registry is
// populated at app startup from IndexedDB; render-time lookups (getPattern,
// createCanvasPattern) consult the in-memory registry so the render path
// stays synchronous.

/** @typedef {{ id: string, label: string, width: 32, height: 32, data: number[], isDefault: boolean, favorite: boolean, createdAt: string }} Pattern */

export const PATTERN_SIZE = 32;

// Build a flat 32×32 row-major array from a string template. '#' = 1, anything
// else = 0. Whitespace is stripped. Accepts a smaller (w × h) template and
// tiles it up to 32×32 so we can describe patterns compactly.
function tile32(template, w = PATTERN_SIZE, h = PATTERN_SIZE) {
  const chars = template.replace(/\s/g, '');
  if (chars.length !== w * h) {
    throw new Error(`pattern template: expected ${w * h} chars, got ${chars.length}`);
  }
  const out = new Array(PATTERN_SIZE * PATTERN_SIZE);
  for (let y = 0; y < PATTERN_SIZE; y++) {
    for (let x = 0; x < PATTERN_SIZE; x++) {
      out[y * PATTERN_SIZE + x] = chars[(y % h) * w + (x % w)] === '#' ? 1 : 0;
    }
  }
  return out;
}

