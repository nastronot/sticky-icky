// Legacy IDs from before the patterns store existed. Saved designs / in-flight
// layer objects reference the short names; the layer-load shim rewrites them
// into the new default-* ids so everything downstream deals with the new id
// space.
export const LEGACY_PATTERN_ID_MAP = {
  'solid':            'default-solid',
  'gray-fine':        'default-gray-fine',
  'gray-mid':         'default-gray-mid',
  'gray-coarse':      'default-gray-coarse',
  'horizontal-lines': 'default-horizontal-lines',
  'vertical-lines':   'default-vertical-lines',
  'diagonal-lines':   'default-diagonal-lines',
  'grid':             'default-grid',
  'cross':            'default-cross',
  'brick':            'default-brick',
  'waves':            'default-waves',
  'diamonds':         'default-diamonds',
};

/** Map a legacy (or unknown) pattern id to a known registry id. Unknown ids
 *  collapse to default-solid. */
export function mapLegacyPatternId(id) {
  if (!id) return 'default-solid';
  if (LEGACY_PATTERN_ID_MAP[id]) return LEGACY_PATTERN_ID_MAP[id];
  return id; // a default-* or custom-* id; registry lookup handles unknown
}

// ── In-memory pattern registry (source of truth for render-time lookups) ────

let registry = new Map(DEFAULT_PATTERNS.map(p => [p.id, {
  ...p,
  width: PATTERN_SIZE,
  height: PATTERN_SIZE,
  isDefault: true,
  favorite: false,
  createdAt: '',
}]));

/** Replace the entire registry. Called once on app startup from App after
 *  loading patterns out of IndexedDB, and again whenever the user adds /
 *  edits / deletes / restores patterns. Invalidates the canvas pattern cache
 *  so subsequent renders use the fresh bitmaps. */
export function setPatternsRegistry(patterns) {
  registry = new Map(patterns.map(p => [p.id, p]));
  patternCanvasCache.clear();
}

/** Read the full registry as an array (favorites-last sort handled by caller). */
export function getAllPatterns() {
  return Array.from(registry.values());
}

/** Look up a pattern by id. Falls back through the legacy shim, then to
 *  default-solid, then to the first seed entry so renders never crash. */
export function getPattern(id) {
  if (!id) return registry.get('default-solid') ?? DEFAULT_PATTERNS[0];
  const direct = registry.get(id);
  if (direct) return direct;
  const mapped = LEGACY_PATTERN_ID_MAP[id];
  if (mapped) {
    const viaMap = registry.get(mapped);
    if (viaMap) return viaMap;
  }
  return registry.get('default-solid') ?? DEFAULT_PATTERNS[0];
}

// ── Canvas pattern cache ─────────────────────────────────────────────────────

const patternCanvasCache = new Map();

/** Explicit cache invalidation, exported for the rare callers that know a
 *  specific pattern's bitmap has changed (e.g. the pattern editor's preview). */
export function invalidatePatternCache(id) {
  if (id === undefined) patternCanvasCache.clear();
  else patternCanvasCache.delete(id);
}

/** Return a CanvasPattern for the given pattern id, suitable for use as
 *  ctx.fillStyle. Tiles at 1:1 pixel resolution. */
export function createCanvasPattern(ctx, patternId) {
  const pat = getPattern(patternId);
  const cacheKey = pat.id;
  if (patternCanvasCache.has(cacheKey)) return patternCanvasCache.get(cacheKey);

  const tile = document.createElement('canvas');
  tile.width = pat.width;
  tile.height = pat.height;
  const tCtx = tile.getContext('2d');
  tCtx.imageSmoothingEnabled = false;
  const img = tCtx.createImageData(pat.width, pat.height);
  for (let i = 0; i < pat.data.length; i++) {
    const px = i * 4;
    if (pat.data[i]) {
      img.data[px]     = 0;
      img.data[px + 1] = 0;
      img.data[px + 2] = 0;
      img.data[px + 3] = 255;
    } else {
      img.data[px + 3] = 0;
    }
  }
  tCtx.putImageData(img, 0, 0);

  const canvasPat = ctx.createPattern(tile, 'repeat');
  patternCanvasCache.set(cacheKey, canvasPat);
  return canvasPat;
}
