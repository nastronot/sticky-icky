// Built-in 1-bit tileable fill patterns. Each pattern is a bitmap that tiles
// across a layer's bounding box. Data is row-major: 1 = on (prints black),
// 0 = off (transparent). Tile sizes are 16–32px for visual legibility at
// 203 DPI (a 32×32 tile is ~0.16" on paper).

/** @typedef {{ id: string, label: string, width: number, height: number, data: number[] }} Pattern */

// Helper: generate a flat row-major array from a string template where
// '#' = 1 and '.' = 0. Whitespace is stripped.
function p(w, h, template) {
  const chars = template.replace(/\s/g, '');
  const data = [];
  for (let i = 0; i < w * h; i++) {
    data.push(chars[i] === '#' ? 1 : 0);
  }
  return { width: w, height: h, data };
}

